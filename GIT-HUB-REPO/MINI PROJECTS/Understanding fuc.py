def fun2(*args): # single asterisk(*) is used to take n number of inputs
    return sum(args) #sum is used to add all the values which we will enter inside the fun2
print(fun2(1,2,3,4,5))#input taken by the user to sum them

#def = long-term use.
# lambda = short & temporary.
# args = flexible list of inputs.
